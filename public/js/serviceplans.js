//service plan 1 on hover - mouseover & mouseout
document.getElementById('service-plan-1').addEventListener('mouseover', function() {
    console.log('what what?!');
    document.getElementById('service-plan-1').style.backgroundColor = '#57C8FF';
})

document.getElementById('service-plan-1').addEventListener('mouseout', function() {
    console.log('bye bye! have a good time!');
    document.getElementById('service-plan-1').style.backgroundColor = '#2A9CC2';

})

